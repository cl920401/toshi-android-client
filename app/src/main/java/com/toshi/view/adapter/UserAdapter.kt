package com.toshi.view.adapter

import android.support.v7.widget.RecyclerView
import android.view.LayoutInflater
import android.view.ViewGroup
import com.toshi.R
import com.toshi.model.network.user.UserV2
import com.toshi.view.adapter.viewholder.UserViewHolder

class UserAdapter(
        val onItemClickListener: (UserV2) -> Unit
) : RecyclerView.Adapter<UserViewHolder>() {

    private val users by lazy { mutableListOf<UserV2>() }

    fun setUsers(users: List<UserV2>) {
        this.users.clear()
        this.users.addAll(users)
        notifyDataSetChanged()
    }

    override fun onCreateViewHolder(parent: ViewGroup, viewType: Int): UserViewHolder {
        val itemView = LayoutInflater.from(parent.context).inflate(R.layout.list_item__user, parent, false)
        return UserViewHolder(itemView)
    }

    override fun onBindViewHolder(holder: UserViewHolder, position: Int) {
        val user = users[position]
        holder.apply {
            setUser(user)
            setOnItemClickListener(onItemClickListener, user)
        }
    }

    override fun getItemCount(): Int = users.size
}
